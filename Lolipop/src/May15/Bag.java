package May15;

public class Bag {
	private String color;
	private int Price;
	//setter methods
	public void setcolor(String color) {
		this.color = color;
	}
	public void setprice(int Price) {
		this.Price = Price;
	}
	//getter methods
	public String getcolor() {
		return this.color;
	}
	public int getPrice() {
		return this.Price;
	}
}
